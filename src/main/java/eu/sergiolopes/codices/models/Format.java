package eu.sergiolopes.codices.models;

public class Format {

    private int id;
    private String type;
    private String name;

    public Format(String type, String name) {
        this(0, type, name);
    }

    public Format(int id, String type, String name) {
        this.id = id;
        this.type = type;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String toString() {
        return name;
    }
}

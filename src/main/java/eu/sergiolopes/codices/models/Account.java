package eu.sergiolopes.codices.models;

/**
 * Manages different users of the application, offering some type of account management for each user that owns items (
 * books, ebooks, audiobooks). When integrated with WEB based version of Codices, the account data is used to sync users
 * between both systems.
 */
public class Account {
    //TODO: Review created and updated properties; check string vs. date usage.

    /**
     * Record's primary key. When 0 (zero) the record has not been saved to storage and exists only in memory.
     */
    private int id;
    private String username;
    private String email;
    private String name;
    private boolean active;
    private String password;
    private String resetToken;
    private String createdAt;
    private String updatedAt;
    private String authKey;

    public Account(int id, String username) {
        this(0, username, null, null, true, null);
    }

    public Account(int id, String username, String email, String name, boolean active, String password) {
        this.id = id;
        this.username = username;
        this.email = email;
        this.name = name;
        this.active = active;
        this.password = password;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getResetToken() {
        return resetToken;
    }

    public void setResetToken(String resetToken) {
        this.resetToken = resetToken;
    }

    public String getAuthKey() {
        return authKey;
    }

    public void setAuthKey(String authKey) {
        this.authKey = authKey;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(String updatedAt) {
        this.updatedAt = updatedAt;
    }
}
